/**
 * gen-build-info.js — stamps the build with version + git metadata.
 *
 * Writes src/buildInfo.js (consumed by the Settings screen) from:
 *   - the "version" field in package.json (the marketing/semver version), and
 *   - git: total commit count (build number) + short SHA + the build date.
 *
 * Run automatically before bundling/building (see package.json `bundle:android`
 * and the CI workflow), so the in-app version number updates on every push.
 * Git failures fall back to safe defaults so the bundle never fails to build.
 */
const fs = require('fs');
const path = require('path');
const {execFileSync} = require('child_process');

const root = path.resolve(__dirname, '..');

function pkgVersion() {
  try {
    const pkg = JSON.parse(fs.readFileSync(path.join(root, 'package.json'), 'utf8'));
    return pkg.version || '1.0.0';
  } catch (e) {
    return '1.0.0';
  }
}

// Fixed argument arrays only (no shell, no interpolation) — safe by construction.
function git(args, fallback) {
  try {
    return execFileSync('git', args, {
      cwd: root,
      stdio: ['ignore', 'pipe', 'ignore'],
    })
      .toString()
      .trim();
  } catch (e) {
    return fallback;
  }
}

const version = pkgVersion();
const build = parseInt(git(['rev-list', '--count', 'HEAD'], '0'), 10) || 0;
const commit = git(['rev-parse', '--short', 'HEAD'], 'local');
const builtAt = new Date().toISOString().slice(0, 10);

const contents = `/**
 * buildInfo.js — AUTO-GENERATED by scripts/gen-build-info.js. Do not edit.
 * Regenerated on every bundle/build so the Settings version reflects each push.
 */
export default {
  version: '${version}',
  build: ${build},
  commit: '${commit}',
  builtAt: '${builtAt}',
};
`;

const outPath = path.join(root, 'src', 'buildInfo.js');
fs.writeFileSync(outPath, contents);
console.log(`Wrote ${path.relative(root, outPath)}: v${version} (build ${build} · ${commit})`);
